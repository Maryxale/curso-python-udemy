#Ejemplo de tipos de datos

#Ejemplo de concatenacion o suma de valores

numero1_cadena = '10'
print(f'Numero 1 en cadena: {numero1_cadena}')
numero2_cadena = '20'
print(f'Numero 2 en cadena: {numero2_cadena}')
resultado = numero1_cadena + numero2_cadena
print(f'concatenacion: {resultado}')

#Conversion a tipo de datos
numero1_entero = int(numero1_cadena)
numero2_entero = int(numero2_cadena)
resultado = numero1_entero + numero2_entero
print(f'suma de estos valores: {resultado}')
#se hace de la misma manera pero si queremos que los valores se sumen tenemos que convertir a entero (int)

